package org.proghax333.testing;

public class BooleanExpressionsVerify {
    public static void main(String[] args) {
        System.out.println(!(5 > 3));
        System.out.println((7 <= 7) && (5 != 5));
        System.out.println((10 / 2 == 6) || (3 * 3 > 10));
        System.out.println((2 * 3 != 6) || (5 >= 5));
    }
}
